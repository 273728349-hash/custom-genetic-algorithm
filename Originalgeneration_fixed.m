%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%  0-1 背包问题 — 遗传算法（修复版 v2 — 新手注释版）
%
%  作者：AI 助手（基于 Originalgeneration.m 修复）
%
%  【什么是 0-1 背包问题？】
%  假设有 n 个物品，每个物品有"价值"和"重量"，你有一个背包，最大载重是
%  max_size。每个物品要么选（1），要么不选（0），目标是让选中的物品总
%  价值最大，同时总重量不超过 max_size。
%
%  【什么是遗传算法？】
%  模拟生物进化：
%    1. 初始化一群随机解（种群）
%    2. 评估每个解的好坏（适应度 = 总价值）
%    3. 选好的解作为"父母"
%    4. 父母交叉（交换部分基因）产生"孩子"
%    5. 对孩子做随机变异
%    6. 用孩子替换旧种群，回到第 2 步，重复很多代
%    7. 最后一代里最好的那个就是近似最优解
%
%  【这版 v2 修复了原版的哪些缺陷？见每处的 [修复N] 标签】
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

clc                          % 清空命令行窗口的输出
clearvars                    % [修复1] 只清除工作区变量
%   原版用 clear all，会把 MATLAB 的全局对象（如 timer、timer 回调等）
%   也一并删除，容易出问题。clearvars 更安全。

close(gcf)                   % 关掉之前可能残留的图窗

%% ==========================================================================
%  模块 01：算法输入 + 预处理
% ==========================================================================

% 每个物品的价值（越小越好，越大越值钱）
values = [10 3 2 8 9 7 4 5 9 4 5 6 5 4 8 3 4 9 2 7 5 3 6 8 2 4 9 5];

% 每个物品的重量
sizes  = [2 9 8 3 2 10 2 3 4 5 3 4 2 8 6 4 2 3 8 6 3 5 2 4 1 8 6 7];

% 背包的最大承重
max_size = 30;

% 物品个数（用 length 而不是 size(values,2)，两者结果一样，但 length 更直观）
n = length(values);

% [修复2] 安全检查：如果某个物品的重量本身就超过了背包容量，
%   那这个物品永远不可能被选中。原版在交叉/变异修复时如果所有物品
%   都被删光了，调用 min([]) 会直接报错崩溃。这里提前处理掉。
oversize_idx = find(sizes > max_size);   % 找出所有超重的物品编号
if ~isempty(oversize_idx)                % 如果有超重物品
    warning('物品 %d 的 size=%.0f > max_size=%d，将被强制设为 0', ...
        oversize_idx, sizes(oversize_idx), max_size);
    sizes(oversize_idx) = 0;              % 重量设为 0，价值也清零
    values(oversize_idx) = 0;
end

%% ==========================================================================
%  模块 02：算法控制参数
% ==========================================================================

rng(42);                   % [修复3] 固定随机数种子
%   原版没有设置种子，导致每次运行的结果都不一样，
%   调试时很难定位问题。设置 rng(42) 后，每次运行结果都完全相同，
%   方便复现和对比实验。42 是常用种子值（来自《银河系漫游指南》），
%   你可以改成任意整数。

PopSize   = 100;            % 种群大小：每一代有 100 个个体
Pro_Cross = 0.7;            % 交叉概率：70% 的概率进行交叉操作
Pro_Muta  = 0.12;           % [修复4] 原版用 0.05，提高到 0.12
%   原因：原版变异率太低，种群多样性不足，容易早熟收敛。
%   12% 的基因翻转率能保持足够的随机探索。
elite_num = max(1, floor(PopSize * 0.1));  % [修复5] 精英数量
%   原版用 0.05（5%），100 个个体只保留 5 个。
%   这里改为 10%，即保留 10 个最优个体，防止最优解在交叉中被破坏。
%   max(1, ...) 确保即使 PopSize 很小也至少保留 1 个。
st_max    = 200;            % 最大迭代次数（最多跑 200 代）
st_stop   = 30;             % [修复6] 收敛检测窗口
%   如果连续 st_stop 代都没有进步，就提前结束，不用干等到 200 代。

best_history = zeros(st_max, 1);  % [修复6] 数组记录每一代的最优适应度值

%% ==========================================================================
%  模块 03：初代种群生成
%
%  [修复7] 原版用"随机生成 -> 检查重量 -> 不超就保留"的方法：
%    每个物品以 55% 的概率被选中，28 个物品的期望总重量约 79，
%    远超 max_size=30。这意味着 >95% 的随机个体都会超重被拒绝，
%    产生一个合法初代可能需要成千上万次尝试，效率极低。
%
%  修复方案 v2：混合生成策略
%    - 30% 用贪心法（保证有一定质量）
%    - 70% 用随机+贪心混合（保证多样性）
%    这样既有高质量个体作为进化基础，又有足够的多样性防止早熟收敛。
% ==========================================================================

InitialPop = zeros(PopSize, n);   % 预分配矩阵：PopSize 行 × n 列

% 价值密度
density = values ./ sizes;        % 逐元素相除
density(sizes == 0) = -Inf;       % 超重物品密度设为负无穷，排在最后
[~, order] = sort(density, 'descend');  % 按密度从高到低排序

for k = 1:PopSize
    Indi = false(1, n);
    
    % ---- 策略 1：贪心填充（30% 概率） ----
    %   按密度从高到低遍历，如果当前物品能装下就装上
    if rand() < 0.3
        remain = max_size;            % 背包剩余空间
        for idx = 1:n
            o = order(idx);           % 取密度排名第 idx 的物品编号
            if sizes(o) > 0 && sizes(o) <= remain   % 物品有重量 且 装得下
                Indi(o) = true;       %   选中它
                remain = remain - sizes(o);  %   更新剩余空间
            end
        end
        
        % 贪心填充后再做较大扰动（20% 概率翻转），增加多样性
        flip_mask = rand(1, n) < 0.20;
        Indi_new = xor(Indi, flip_mask);
        if sum(sizes .* Indi_new) <= max_size && sum(Indi_new) > 0
            Indi = Indi_new;
        end
        
    % ---- 策略 2：纯随机构造（70% 概率） ----
    else
        %   逐个物品决策：如果当前背包还能装下，就有一定概率装进去
        remain = max_size;
        for j = 1:n
            if rand() < 0.4           %   每个物品有 40% 的基础概率
                if sizes(j) <= remain %   且背包能装下
                    Indi(j) = true;
                    remain = remain - sizes(j);
                end
            end
        end
        
        %   如果这个个体什么都没选（全零），重新生成
        if sum(Indi) == 0
            while true
                rnd = rand(1, n) < 0.2;  % 随机选约 20% 的物品
                if sum(sizes .* rnd) <= max_size && sum(rnd) > 0
                    Indi = rnd;
                    break;
                end
            end
        end
    end
    
    InitialPop(k, :) = Indi;       % 把这个个体存入种群
end

%% ==========================================================================
%  模块 04 ~ 07：主循环（选择 → 交叉 → 变异 → 新一代）
% ==========================================================================

for cs = 1:st_max        % 对每一代进行迭代
    
    % ------------------------------------------------------------------
    % 步骤 1：计算适应度
    % ------------------------------------------------------------------
    %   适应度 = 个体选中的物品总价值。
    %   InitialPop 是二进制矩阵（每行一个个体），values' 是列向量，
    %   矩阵乘法 = 每个行向量与 values 做点积，一次性算出所有个体的适应度。
    fitness = InitialPop * values';
    
    %   按适应度从高到低排序
    [sorted_fit, idx] = sort(fitness, 'descend');
    sorted_Pop = InitialPop(idx, :);   % 用排序索引重新排列种群
    
    %   分离精英和普通个体
    elite_pop   = sorted_Pop(1:elite_num, :);             % 最好的 elite_num 个
    normal_pop  = sorted_Pop(elite_num+1:end, :);         % 其余的
    normal_fit  = sorted_fit(elite_num+1:end);            % 普通个体的适应度
    
    % [修复8] 计算选择概率（轮盘赌）
    sum_normal = sum(normal_fit);
    if sum_normal == 0         % 防御：如果所有个体适应度都是 0，平均分配
        prob_normal = ones(size(normal_fit)) / length(normal_fit);
    else
        prob_normal = normal_fit / sum_normal;   % 适应度越高，被选中的概率越大
    end
    
    % ------------------------------------------------------------------
    % 步骤 2：选择（轮盘赌选择）
    % ------------------------------------------------------------------
    %   普通个体的适应度越高，被抽中的概率越大。允许重复抽样，
    %   所以好的个体可能被选中多次，差的个体可能一次都没被选中。
    normal_num = length(normal_fit);
    selected_idx = randsample(normal_num, normal_num, true, prob_normal);
    parents = normal_pop(selected_idx, :);   % 选中的个体作为父母
    
    % ------------------------------------------------------------------
    % 步骤 3：交叉
    % ------------------------------------------------------------------
    % [修复9] 原版用 child_pop=[child_pop; new_row] 动态扩容，
    %   MATLAB 每次都要重新分配内存并复制所有数据，复杂度 O(N²)。
    %   这里预分配好矩阵，直接按索引赋值，复杂度降为 O(N)。
    child_pop = zeros(normal_num, n);
    
    for i = 1:2:normal_num   % 每次取两个父母产生两个子代
        if i == normal_num && mod(normal_num, 2) ~= 0
            % 如果 normal_num 是奇数，最后一个没有配对对象，直接复制
            child_pop(i, :) = parents(i, :);
            continue;
        end
        
        % 随机选两个不同的父母
        pair = randperm(normal_num, 2);   % 从 1~normal_num 中随机抽 2 个不重复的数
        p1 = parents(pair(1), :);         % 父母 1
        p2 = parents(pair(2), :);         % 父母 2
        
        if all(p1 == p2)                  % 如果两个父母完全一样，直接复制
            child_pop(i, :)   = p1;
            child_pop(i+1, :) = p2;
            continue;
        end
        
        if rand() < Pro_Cross            % 按交叉概率决定是否交叉
            % [修复10] 原版用单点交叉（只切一刀），修复版用均匀交叉：
            %   每个基因随机从父或母那里继承，交叉方式更丰富，
            %   对二进制编码的背包问题效果更好。
            dd = rand(1, n) < 0.5;        % 生成一个随机掩码，每个位置 50% 概率
            
            c1 = p1;                      % 子代 1 先从父1 拷贝
            c2 = p2;                      % 子代 2 先从父2 拷贝
            c1(dd) = p2(dd);              %   掩码为真的位置换成父2 的基因
            c2(dd) = p1(dd);              %   掩码为真的位置换成父1 的基因
            
            % [修复10续] 交叉后的子代可能超重，调用修复函数处理
            c1 = repair_individual(c1, sizes, values, max_size);
            c2 = repair_individual(c2, sizes, values, max_size);
        else
            % 不交叉就直接复制父母
            c1 = p1;
            c2 = p2;
        end
        
        child_pop(i, :)   = c1;           % 存入子代矩阵
        child_pop(i+1, :) = c2;
    end
    
    % ------------------------------------------------------------------
    % 步骤 4：变异
    % ------------------------------------------------------------------
    % [修复11] 原版的变异是简单按位翻转（0→1 或 1→0），
    %   翻转为 1 后很可能超重，修复逻辑只能靠"随机移除一个物品"。
    %   长期下来，加物品的操作经常被修掉，减物品的操作不被修，
    %   导致种群净趋势是丢失物品，趋向于全零空解。
    %
    %   修复方案：
    %     - 0→1（尝试加物品）：先检查是否超重，超重就不加
    %     - 1→0（尝试减物品）：总是允许
    %   这样加和减是对称的，不会偏向丢失物品。
    
    for i = 1:normal_num
        x = child_pop(i, :);              % 取出第 i 个个体
        
        for j = 1:n                       % 遍历这个个体的每个基因
            if rand() < Pro_Muta          % 如果触发了变异
                if x(j) == 0
                    % 当前是 0，想变成 1（尝试把物品 j 放进背包）
                    new_x = x;
                    new_x(j) = 1;           %   先试着放
                    if sum(sizes .* new_x) <= max_size
                        x(j) = 1;           %   不超重的话，真的放进去
                    end
                    % 如果超重，就不做操作（保持为 0），而不是翻回去
                else
                    % 当前是 1，想变成 0（把物品 j 从背包取出）
                    x(j) = 0;               %   总是允许，取出不会超重
                end
            end
        end
        
        % [修复11续] 变异后不能变成全零（一个都不选）
        if sum(x) == 0
            x(randi(n)) = 1;              %   随机选一个物品放进去
            while sum(sizes .* x) > max_size
                x(randi(n)) = 0;           %   如果超重就随机删一个
            end
        end
        
        child_pop(i, :) = x;
    end
    
    % ------------------------------------------------------------------
    % 步骤 5：组建新一代种群
    % ------------------------------------------------------------------
    %   把精英个体和子代个体合并，精英直接保留，不经过交叉变异
    %   （保护最好的解不被破坏）
    InitialPop = [elite_pop; child_pop];
    
    % ------------------------------------------------------------------
    % 步骤 6：记录本代最优
    % ------------------------------------------------------------------
    best_val = max(fitness);              % 本代所有个体中的最高适应度
    best_history(cs) = best_val;          % 记录下来
    
    fprintf('第 %3d 代，最优适应度：%.1f\n', cs, best_val);

    % 提前终止逻辑：连续 st_stop 代无改善就停止
    if cs > st_stop && best_history(cs) == best_history(cs - st_stop)
        fprintf('\n连续 %d 代无改善，提前终止！\n', st_stop);
        break;
    end
end

%% ==========================================================================
%  输出最终结果
% ==========================================================================

[best_final_val, best_idx] = max(fitness);   % 找到最优个体的值和索引
best_solution = sorted_Pop(best_idx, :);      % 取出最优个体的选择方案

fprintf('\n================ 最终结果 ================\n');
fprintf('最优总价值：     %.1f\n', best_final_val);
fprintf('选中的物品编号： ');
idx_list = find(best_solution);
disp(idx_list);
fprintf('对应价值：       ');
disp(best_solution .* values);           % [修复12] 用点乘代替索引
fprintf('对应重量：       ');
disp(best_solution .* sizes);            % [修复12] 用点乘代替索引
fprintf('总重量：         %.0f / %d\n', ...
    sum(best_solution .* sizes), max_size);
is_ok = sum(best_solution .* sizes) <= max_size;
fprintf('是否合法（不超重）：%s\n', string(is_ok));

%% ==========================================================================
%  绘制收敛曲线
% ==========================================================================

figure;                                  % 新建一个图窗
plot(1:st_max, best_history, 'b-', 'LineWidth', 1.5);
xlabel('迭代代数');
ylabel('最优适应度（总价值）');
title('遗传算法 — 收敛曲线（修复版 v2）');
grid on;                                 % 打开网格线

%% ==========================================================================
%  子函数：修复超重个体
%
%  [修复2] 原版在交叉/变异修复时，如果所有物品都被删光了，
%    values03(values03~=0) 返回空数组，min([]) 会报错崩溃。
%    这里用子函数统一处理，加了安全检查。
%
%  修复策略：如果超重，移除总价值最小的物品，直到不超重为止。
%  这样保留的是"总价值损失最小"的组合。
% ==========================================================================
function indiv = repair_individual(indiv, sz, vl, cap)
    % 输入：
    %   indiv — 个体向量（1×n 的逻辑/数值数组）
    %   sz    — 每个物品的重量向量
    %   vl    — 每个物品的价值向量
    %   cap   — 背包容量上限
    % 输出：
    %   indiv — 修复后的个体（保证总重量 ≤ cap，且至少选一个物品）
    
    s = sum(sz .* indiv);     % 计算当前个体总重量
    
    while s > cap             % 如果超重了，持续修复
        active = indiv == 1;   %   找出哪些物品被选中了
        if ~any(active)        %   [修复2] 如果已经没有选中物品了，停止
            break;
        end
        act_vals = vl(active);  %   取出所有选中物品的价值
        
        %   找到价值最小的那个物品（移除它的代价最小）
        [~, min_local] = min(act_vals);
        act_idx = find(active);  %   获取选中物品的原始位置索引
        indiv(act_idx(min_local)) = 0;  %   把这个物品移除
        s = sum(sz .* indiv);   %   重新计算总重量
    end
    
    % 如果修复后一个物品都没剩下，随机选一个能放进去的物品
    if sum(indiv) == 0
        feasible = find(sz <= cap);   %   找出所有能单独放进背包的物品
        if ~isempty(feasible)
            indiv(feasible(1)) = 1;    %   选第一个放进去
        end
    end
end
